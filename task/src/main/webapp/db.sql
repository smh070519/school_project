-- 고객 테이블
CREATE TABLE CUSTOMER (
    CUST_NO VARCHAR2(10) PRIMARY KEY,
    CUST_NAME VARCHAR2(50),
    PHONE VARCHAR2(20),
    EMAIL VARCHAR2(100)
);

-- 장비 대여 테이블
CREATE TABLE RENTAL (
    RENTAL_NO VARCHAR2(10) PRIMARY KEY,
    CUST_NO VARCHAR2(10),
    RENTAL_DATE DATE,
    EQUIP_CODE VARCHAR2(10),
    RETURN_DUE DATE,
    CONSTRAINT FK_RENTAL_CUST FOREIGN KEY (CUST_NO)
        REFERENCES CUSTOMER(CUST_NO)
);


